/** Retorna a string traduzida. Suporta placeholders {0}, {1}... */
function t(key, ...args) {
    const lang = (window.App && window.App.settings && window.App.settings.language) || 'pt-BR';
    const dict = TRANSLATIONS[lang] || TRANSLATIONS['pt-BR'];
    let str = (dict && dict[key]) || (TRANSLATIONS['pt-BR'] && TRANSLATIONS['pt-BR'][key]) || key;
    args.forEach((arg, i) => { str = str.replace(`{${i}}`, arg); });
    return str;
}

/** Aplica traduções a todos os elementos marcados no DOM. */
function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
}
